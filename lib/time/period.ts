/** Monday-based week start at local midnight. */
export function getWeekStart(date: Date): Date {
  const d = new Date(date);
  const day = d.getDay();
  const diff = d.getDate() - day + (day === 0 ? -6 : 1);
  d.setDate(diff);
  d.setHours(0, 0, 0, 0);
  return d;
}

export function getMonthStart(date: Date): Date {
  const d = new Date(date);
  d.setDate(1);
  d.setHours(0, 0, 0, 0);
  return d;
}

export function getPeriodStart(period: "weekly" | "monthly", date = new Date()) {
  return period === "weekly" ? getWeekStart(date) : getMonthStart(date);
}
